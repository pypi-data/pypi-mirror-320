#提取省市区
def get_ssq(key=None,Index=False):
    """
    :param key: 要提取字符串
    :param Index: 返回省市区所在字符串索引位置
    :return:
    """
    if key:
        getssq= {}
        for k,vs in ssq_d.items():
            for v in vs:
                if v in key:
                    index=key.index(v)
                    getssq[v]=index
                    break
        if Index:
            return getssq
        ssq_list = list(zip(getssq.keys(), getssq.values()))
        return ([v[0] for v in sorted(ssq_list)])
    return ssq_d

