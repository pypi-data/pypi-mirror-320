def 太极图():
    import turtle
    
    angle = 270
    
    def tai():
        r = 200  # 设置半径
        turtle.penup()  # 拿起画笔
        turtle.goto(0, 0)  # 到画布中心
        turtle.setheading(angle)  # 设置当前朝向为angle角度
        turtle.fd(r)  # 前进r的距离
        turtle.pendown()  # 放下画笔
        turtle.right(90)  # 调整海龟角度
    
        # 画阳鱼
        turtle.fillcolor("white")
        turtle.begin_fill()  # 开始填充
        turtle.circle(-r / 2, 180)
        turtle.circle(r / 2, 180)
        turtle.circle(r, 180)
        turtle.end_fill()
    
        # 画阴鱼
        turtle.fillcolor("black")
        turtle.begin_fill()
        turtle.circle(r, 180)
        turtle.right(180)
        turtle.circle(-r / 2, 180)
        turtle.circle(r / 2, 180)
        turtle.end_fill()
    
        # 画阴鱼眼
        turtle.penup()
        turtle.setheading(angle)
        turtle.fd(-r / 2)
        turtle.pendown()
        turtle.dot(r / 4, "white")
    
        # 画阳鱼眼
        turtle.penup()
        turtle.fd(-r)
        turtle.pendown()
        turtle.dot(r / 4, "black")
        turtle.penup()
    
    turtle.tracer(0)
    for i in range(10000000):
        tai()
        turtle.update()
        turtle.clear()
        angle += 1

def 奥运五环():
    import turtle 

    def draw_a_circle(x, y, color, radius=70, pensize=10):
        turtle.penup()
        turtle.goto(x, y)
        turtle.pensize(pensize)
        turtle.pencolor(color)
        turtle.pendown()
        turtle.circle(radius)
        turtle.penup()

    circles = [
        (-130, 75, "blue"),
        (-10, 75, "black"),
        (110, 75, "red"),
        (-60, -20, "yellow"),
        (60, -20, "green")
    ]

    for x, y, color in circles:
        draw_a_circle(x, y, color)

    turtle.done()

